#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	while (n)
	{
		if (*(unsigned const char *)s == (unsigned const char)c)
			return ((void *)s);
		s++;
		n--;
	}
	return (NULL);
}

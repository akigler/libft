char	*ft_strcpy(char *dst, char *src)
{
	int		i;

	i = 0;
	while (*src)
	{
		*dst = *src;
		dst++;
		src++;
		i++;
	}
	if (*src == '\0')
		*dst = '\0';
	return (dst - i);
}
